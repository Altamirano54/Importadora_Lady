    private void BTRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTRegistrarActionPerformed
        // Validar que los campos no estÃ©n vacÃ­os
        if (TFNombre.getText().isEmpty() || TFTelefono.getText().isEmpty() || TFNroDocumento.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos.", "Campos VacÃ­os", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (clienteSeleccionado == null) {
            // --- MODO REGISTRO ---
            Cliente nuevoCliente = new Cliente();
            nuevoCliente.setNombre(TFNombre.getText());
            nuevoCliente.setTelefono(TFTelefono.getText());
            nuevoCliente.setTipo_documento(mctd.getSeleccionado());
            nuevoCliente.setNro_documento(TFNroDocumento.getText());

            if (clienteManager.registrarCliente(nuevoCliente)) {
                JOptionPane.showMessageDialog(this, "Cliente registrado exitosamente.", "Registro Exitoso", JOptionPane.INFORMATION_MESSAGE);
                CargarTabla();
                limpiarFormulario();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo registrar el cliente.", "Error de Registro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // --- MODO MODIFICACIÃN ---
            clienteSeleccionado.setNombre(TFNombre.getText());
            clienteSeleccionado.setTelefono(TFTelefono.getText());
            clienteSeleccionado.setTipo_documento(mctd.getSeleccionado());
            clienteSeleccionado.setNro_documento(TFNroDocumento.getText());

            if (clienteManager.actualizarCliente(clienteSeleccionado.getId(), clienteSeleccionado)) {
                JOptionPane.showMessageDialog(this, "Cliente actualizado exitosamente.", "ActualizaciÃ³n Exitosa", JOptionPane.INFORMATION_MESSAGE);
                CargarTabla();
                limpiarFormulario();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo actualizar el cliente.", "Error de ActualizaciÃ³n", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_BTRegistrarActionPerformed

    private void BTEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTEliminarActionPerformed
        // TODO add your handling code here:
        if (clienteSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente de la tabla.", "SelecciÃ³n Requerida", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(
                this,
                "Â¿EstÃ¡ seguro de que desea eliminar al cliente '" + clienteSeleccionado.getNombre() + "'?",
                "Confirmar EliminaciÃ³n",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE
        );

        if (confirmacion == JOptionPane.YES_OPTION) {
            if (clienteManager.eliminarCliente(clienteSeleccionado.getId())) {
                JOptionPane.showMessageDialog(this, "Cliente eliminado exitosamente.", "EliminaciÃ³n Exitosa", JOptionPane.INFORMATION_MESSAGE);
                CargarTabla();
                limpiarFormulario();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar el cliente.", "Error de EliminaciÃ³n", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BTEliminarActionPerformed

