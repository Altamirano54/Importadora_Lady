    public BufferedImage obtenerImagenDeLabel() {
        if (LBImagenEmpleado.getIcon() != null && LBImagenEmpleado.getIcon() instanceof ImageIcon) {
            Image imagen = ((ImageIcon) LBImagenEmpleado.getIcon()).getImage();

            // Crear un BufferedImage compatible con la imagen
            BufferedImage buffered = new BufferedImage(
                    imagen.getWidth(null),
                    imagen.getHeight(null),
                    BufferedImage.TYPE_INT_ARGB // Puedes usar TYPE_INT_RGB si no necesitas transparencia
            );

            // Dibujar la imagen en el BufferedImage
            buffered.getGraphics().drawImage(imagen, 0, 0, null);
            return buffered;
        }
        return null; // No hay imagen en el label
    }
    
    private void cargarDatosEmpleado(Empleado emp) {
        TFNombre.setText(emp.getNombre());
        TFNroDocumento.setText(emp.getNroDocumento());
        jDateChooser1.setDate(emp.getFechaNacimiento());

        // Seleccionar el tipo de documento
        int indexTipoDoc = modeloComboboxTipoDocumento.getSelectTipoDocumento(emp.getTipoDocumento());
        CBTipoDocumento.setSelectedIndex(indexTipoDoc);

        // Seleccionar el cargo
        int indexCargo = comboboxCargo.getSelectCargo(emp.getCargo());
        CBCrago.setSelectedIndex(indexCargo);

        if (emp.getGenero() == 'M') {
            rbMasculino.setSelected(true);
        } else if (emp.getGenero() == 'F') {
            rbFemenino.setSelected(true);
        }

        TFDIreccion.setText(emp.getDireccion());
        TFTelefono.setText(emp.getTelefono());
        TFCorreo.setText(emp.getCorreo());
        TFUsuario.setText(emp.getUsuario());

        // Imagen
        if (emp.getUrl() != null && !emp.getUrl().isEmpty()) {
            File archivo = new File(emp.getUrl());
            if (archivo.exists()) {
                try {
                    BufferedImage imagen = ImageIO.read(archivo);
                    Image escalada = imagen.getScaledInstance(LBImagenEmpleado.getWidth(), LBImagenEmpleado.getHeight(), Image.SCALE_SMOOTH);
                    LBImagenEmpleado.setIcon(new ImageIcon(escalada));
                    LBImagenEmpleado.setText("");
                } catch (Exception e) {
                    System.err.println("Error al cargar imagen del empleado: " + e.getMessage());
                }
            }
        }
    }
    

