private void cargarReporte(String tipo) {
    switch (tipo) {
        case "ProductosMasVendidos" -> {
            var datos = ReporteManager.obtenerTopProductos();
            var modelo = new ModeloTablaReporte_ProductoMasVendido();
            modelo.setListado(datos);
            TableReports.setModel(modelo);
        }
        case "VentasPorDia" -> {
            var datos = ReporteManager.obtenerVentasPorDia();
            var modelo = new ModeloTablaReporte_VentaPorDia();
            modelo.setListado(datos);
            TableReports.setModel(modelo);
        }
        case "VentasPorMes" -> {
            var datos = ReporteManager.obtenerVentasPorMes();
            var modelo = new ModeloTablaReporte_VentaPorMes();
            modelo.setListado(datos);
            TableReports.setModel(modelo);
        }
        case "VentasPorCliente" -> {
            var datos = ReporteManager.obtenerVentasPorCliente();
            var modelo = new ModeloTablaReporte_VentaPorCliente();
            modelo.setListado(datos);
            TableReports.setModel(modelo);
        }
        case "ClientesPorMes" -> {
            var datos = ReporteManager.obtenerClientesPorMes();
            var modelo = new ModeloTablaReporte_ClientesPorMes();
            modelo.setListado(datos);
            TableReports.setModel(modelo);
        }
    }
}


