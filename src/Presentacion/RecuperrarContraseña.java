    private void iniciarCronometro() {
        segundosRestantes = 60;
        jLabel4.setText("01:00");

        cronometro = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                segundosRestantes--;

                // Formatear minutos y segundos
                int minutos = segundosRestantes / 60;
                int segundos = segundosRestantes % 60;
                String tiempo = String.format("%02d:%02d", minutos, segundos);
                jLabel4.setText(tiempo);

                if (segundosRestantes <= 0) {
                    cronometro.stop();
                    // Acción cuando termina el cronómetro
                    jLabel4.setText("¡Tiempo agotado!");
                    TFCodigo.setEnabled(false); // Bloquea el ingreso del código
                    
                    lm.codigo="";
                    if(pasoActual==2){
                        jButton1.setEnabled(false); // Desactiva el botón "Siguiente"
                        JOptionPane.showMessageDialog(null, "El tiempo ha expirado. Vuelva a solicitar el código.");
                    }
                    
                }
            }
        });

        cronometro.start();
    }
    
    

