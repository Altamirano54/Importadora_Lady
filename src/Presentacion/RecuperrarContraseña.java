    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        switch (pasoActual) {
            case 1:
                // Mostrar siguiente paso: Verificar código
                
                if(TFNroDocumento.getText()!="" && TFUsuario.getText()!=""){
                    empleado=lm.buscarEmpleado(TFUsuario.getText(), TFNroDocumento.getText());
                    
                }else{
                    return;
                }
                if(empleado!=null){
                    lm.enviarCodigo(empleado.getCorreo());
                    pasoActual = 2;
                    iniciarCronometro();
                    LayeredValidarUsuario.setVisible(false);
                    LayeredVerificarCodigo.setVisible(true);
                    LayeredCambioContraseña.setVisible(false);
                }
                
                
                break;

            case 2:
                // Mostrar último paso: Cambio de contraseña
                
                if(lm.codigo.equals(TFCodigo.getText())){
                    pasoActual = 3;
                    jButton1.setText("Finalizar");  
                    LayeredValidarUsuario.setVisible(false);
                    LayeredVerificarCodigo.setVisible(false);
                    LayeredCambioContraseña.setVisible(true);
                    
                }else{
                    JOptionPane.showMessageDialog(null, "Codigo einvalido.");
                }
                
                break;

            case 3:
                String pass1 = new String(PFContraseña.getPassword());
                String pass2 = new String(PFConfirmarContraseña.getPassword());

                if (pass1.equals(pass2)) {
                    try {
                        lm.CambiarContraseña(empleado, pass1);  // Método que llama a BDEmpleado.cambiarContraseña()
                        JOptionPane.showMessageDialog(null, "La contraseña se cambió correctamente.");

                        this.dispose(); // Cierra la ventana actual
                        SwingUtilities.invokeLater(() -> new Login().setVisible(true)); // Vuelve al login
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(null, "Error al cambiar la contraseña: " + ex.getMessage());
                        ex.printStackTrace();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden.");
                }
                // Aquí podrías guardar la nueva contraseña o cerrar la ventana
                // JOptionPane.showMessageDialog(this, "Contraseña cambiada correctamente.");
                 // Cierra la ventana
                break;
        }
        this.pack(); // Ajusta el tamaño
        this.setLocationRelativeTo(null); // Centra la ventana
    }//GEN-LAST:event_jButton1ActionPerformed

